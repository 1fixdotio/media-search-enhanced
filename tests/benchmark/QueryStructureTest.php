<?php
/**
 * Benchmark tests for query structure and performance.
 *
 * @group benchmark
 */
class QueryStructureTest extends WP_UnitTestCase {

	/**
	 * @var int[] Attachment IDs created during seeding.
	 */
	private static $attachment_ids = array();

	/**
	 * Seed the database once for all benchmark tests.
	 */
	public static function wpSetUpBeforeClass( $factory ) {
		$count = 500;

		for ( $i = 0; $i < $count; $i++ ) {
			$id = $factory->attachment->create( array(
				'post_title'     => "benchmark-attachment-{$i}",
				'post_status'    => 'inherit',
				'post_mime_type' => ( $i % 3 === 0 ) ? 'image/png' : 'image/jpeg',
				'post_content'   => "Description for benchmark attachment {$i}",
				'post_excerpt'   => "Caption for attachment {$i}",
			) );

			update_post_meta( $id, '_wp_attachment_image_alt', "Alt text for benchmark image {$i}" );
			update_post_meta( $id, '_wp_attached_file', "2024/01/benchmark-file-{$i}.jpg" );

			self::$attachment_ids[] = $id;
		}

		// Assign taxonomy terms to a subset.
		register_taxonomy( 'media_tag', 'attachment', array( 'public' => true ) );
		$terms = array( 'landscape', 'portrait', 'product', 'team', 'event' );
		foreach ( $terms as $term_name ) {
			wp_insert_term( $term_name, 'media_tag' );
		}
		foreach ( self::$attachment_ids as $idx => $id ) {
			if ( $idx % 5 === 0 ) {
				$term_name = $terms[ $idx % count( $terms ) ];
				wp_set_object_terms( $id, $term_name, 'media_tag' );
			}
		}
	}

	/**
	 * Capture the SQL generated by a search query.
	 *
	 * @param string $search The search term.
	 * @return array { sql: string, time: float, results_count: int }
	 */
	private function capture_query( $search ) {
		global $wpdb, $wp_query;

		$wpdb->queries = array();
		if ( ! defined( 'SAVEQUERIES' ) ) {
			define( 'SAVEQUERIES', true );
		}

		$args = array(
			'post_type'   => 'attachment',
			'post_status' => 'inherit',
			's'           => $search,
			'fields'      => 'ids',
		);

		// The plugin reads from global $wp_query->query_vars.
		$original_vars        = $wp_query->query_vars;
		$wp_query->query_vars = $args;

		$start = microtime( true );

		$query = new WP_Query( $args );

		$elapsed = microtime( true ) - $start;

		$wp_query->query_vars = $original_vars;

		// Find the main query SQL (match on the first comma-separated term if present).
		$search_needle = trim( explode( ',', $search )[0] );
		$captured_sql  = '';
		foreach ( $wpdb->queries as $q ) {
			if ( stripos( $q[0], $search_needle ) !== false && stripos( $q[0], 'SELECT' ) !== false ) {
				$captured_sql = $q[0];
				break;
			}
		}

		return array(
			'sql'           => $captured_sql,
			'time'          => $elapsed,
			'results_count' => $query->found_posts,
		);
	}

	/**
	 * Assert query structure after #10 Phase 1 refactor.
	 *
	 * Phase 1: EXISTS subqueries replace LEFT JOINs, DISTINCT removed.
	 * Phase 2: add assertion for ID = instead of ID LIKE
	 */
	public function test_query_does_not_use_distinct() {
		$result = $this->capture_query( 'benchmark-attachment' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );
		$this->assertStringNotContainsString( 'DISTINCT', $result['sql'], 'Query should not use DISTINCT after EXISTS refactor.' );
	}

	public function test_query_does_not_use_left_join_postmeta() {
		$result = $this->capture_query( 'benchmark-attachment' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );
		$this->assertDoesNotMatchRegularExpression(
			'/LEFT JOIN\s+\S+postmeta\s+AS\s+mse_pm/i',
			$result['sql'],
			'Query should not LEFT JOIN postmeta after EXISTS refactor.'
		);
	}

	public function test_query_uses_exists_subqueries() {
		$result = $this->capture_query( 'benchmark-attachment' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );

		$exists_count = substr_count( $result['sql'], 'EXISTS' );
		$this->assertGreaterThanOrEqual( 2, $exists_count, 'Query should have at least 2 EXISTS subqueries (alt text + filename).' );

		// Verify postmeta EXISTS subqueries are correlated to the outer query.
		$this->assertMatchesRegularExpression(
			'/EXISTS\s*\(\s*SELECT\s+1\s+FROM\s+\S+postmeta\s+WHERE\s+\S+postmeta\.post_id\s*=\s*\S+posts\.ID/i',
			$result['sql'],
			'EXISTS subquery should correlate postmeta.post_id to posts.ID.'
		);

		// Verify taxonomy EXISTS subquery is correlated.
		$this->assertMatchesRegularExpression(
			'/EXISTS\s*\(\s*SELECT\s+1\s+FROM\s+\S+term_relationships\s+AS\s+tr\b.*?tr\.object_id\s*=\s*\S+posts\.ID/is',
			$result['sql'],
			'Taxonomy EXISTS subquery should correlate tr.object_id to posts.ID.'
		);
	}

	public function test_numeric_search_uses_id_equals() {
		$result = $this->capture_query( '12345' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );
		$this->assertDoesNotMatchRegularExpression(
			'/\.ID\s+LIKE/i',
			$result['sql'],
			'Numeric search should not use ID LIKE.'
		);
		$this->assertMatchesRegularExpression(
			'/\.ID\s*=\s*\'?12345/i',
			$result['sql'],
			'Numeric search should use ID = integer comparison.'
		);
	}

	public function test_non_numeric_search_skips_id_match() {
		$result = $this->capture_query( 'benchmark-attachment' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );
		$this->assertDoesNotMatchRegularExpression(
			'/\.ID\s+LIKE/i',
			$result['sql'],
			'Non-numeric search should not use ID LIKE.'
		);
		$this->assertDoesNotMatchRegularExpression(
			'/\.ID\s*=/i',
			$result['sql'],
			'Non-numeric search should not use ID = either.'
		);
	}

	/**
	 * Assert multi-term (comma-separated) search generates OR groups.
	 */
	public function test_multi_term_search_generates_or_groups() {
		add_filter( 'mse_is_media_modal_request', '__return_true' );

		$result = $this->capture_query( 'alpha-term, beta-term' );

		remove_filter( 'mse_is_media_modal_request', '__return_true' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );

		// Both terms should appear in the SQL.
		$this->assertStringContainsString( 'alpha-term', $result['sql'], 'SQL should contain first search term.' );
		$this->assertStringContainsString( 'beta-term', $result['sql'], 'SQL should contain second search term.' );

		// Should have multiple EXISTS groups (one set per term).
		$exists_count = substr_count( $result['sql'], 'EXISTS' );
		$this->assertGreaterThanOrEqual( 4, $exists_count, 'Multi-term search should have EXISTS subqueries for each term (at least 2 per term).' );
	}

	/**
	 * Log EXPLAIN output and timing for manual review.
	 * No assertions — optimizer output varies across MySQL/MariaDB versions.
	 */
	public function test_log_explain_and_timing() {
		global $wpdb;

		$result = $this->capture_query( 'benchmark-attachment' );

		$this->assertNotEmpty( $result['sql'], 'Should have captured the search SQL.' );

		// Run EXPLAIN.
		$explain = $wpdb->get_results( 'EXPLAIN ' . $result['sql'] );

		// Log results for manual review.
		$log  = "\n=== Benchmark Results ===\n";
		$log .= sprintf( "Results found: %d\n", $result['results_count'] );
		$log .= sprintf( "Query time: %.4f seconds\n", $result['time'] );
		$log .= "SQL:\n" . $result['sql'] . "\n\n";
		$log .= "EXPLAIN output:\n";

		if ( ! empty( $explain ) ) {
			$headers = array_keys( get_object_vars( $explain[0] ) );
			$log    .= implode( "\t", $headers ) . "\n";
			foreach ( $explain as $row ) {
				$log .= implode( "\t", array_values( get_object_vars( $row ) ) ) . "\n";
			}
		}

		$log .= "=========================\n";

		// PHPUnit will display this when run with --verbose or --debug.
		fwrite( STDERR, $log );

		// Trivial assertion so PHPUnit doesn't mark this as risky.
		$this->assertTrue( true );
	}
}
